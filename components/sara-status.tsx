export function SaraStatus() {
  const { saraConfig } = useAppContext()
  const { status, statusInfo, errorInfo } = saraConfig

  return (
    <div className="border-solid border-2 rounded-md p-1 flex flex-row">
      {appStatausReducer(saraConfig, status)}
      <div className="pr-1">{status !== 'ERROR' ? statusInfo : errorInfo}</div>
    </div>
  )
}
